CREATE TABLE ADDRESS (
    "ID" integer NOT NULL,
    STREET varchar(100),
    "NUMBER" integer,
    CITY varchar(100),
    STATE varchar(100),
    COUNTRY varchar(100),
	CONSTRAINT address_pkey PRIMARY KEY ("ID")
)
\
CREATE TABLE BROKER (
    "ID" integer NOT NULL,
    "NAME" varchar(100),
    LASTNAME varchar(100),
    DATEOFBIRTH date,
    SSN varchar(100),
    ADDRESSID integer,
	CONSTRAINT broker_pkey PRIMARY KEY ("ID")
)
\
CREATE TABLE BROKERWORKSFOR (
    BROKERID integer NOT NULL,
    COMPANYID integer,
    CLIENTID integer
)
\
CREATE TABLE CLIENT (
    "ID" integer NOT NULL,
    "NAME" varchar(100),
    LASTNAME varchar(100),
    DATEOFBIRTH date,
    SSN varchar(100),
    ADDRESSID integer,
	CONSTRAINT client_pkey PRIMARY KEY ("ID")
)
\
CREATE TABLE COMPANY (
    "ID" integer NOT NULL,
    "NAME" varchar(100),
    MARKETSHARES integer,
    NETWORTH double precision,
    ADDRESSID integer,
	CONSTRAINT company_pkey PRIMARY KEY ("ID")
)
\
CREATE TABLE STOCKBOOKLIST (
    "DATE" timestamp NOT NULL,
    STOCKID integer,
	CONSTRAINT stockbooklist_pkey PRIMARY KEY ("DATE")
)
\
CREATE TABLE STOCKINFORMATION (
    "ID" integer NOT NULL,
    NUMBEROFSHARES integer,
    SHARETYPE number(1),
    COMPANYID integer,
    DESCRIPTION varchar(1000),
	CONSTRAINT stockinformation_pkey PRIMARY KEY ("ID")
)
\
CREATE TABLE "TRANSACTION" (
    "ID" integer NOT NULL,
    "DATE" timestamp,
    STOCKID integer,
    "TYPE" number(1),
    BROKERID integer,
    FORCLIENTID integer,
    FORCOMPANYID integer,
    AMOUNT number(10, 5),
	CONSTRAINT transaction_pkey PRIMARY KEY ("ID")
)
